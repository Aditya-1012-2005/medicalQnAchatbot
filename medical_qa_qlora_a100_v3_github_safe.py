def ask_medical_chatbot(question: str,
                        max_new_tokens: int = 512,
                        temperature: float = 0.3) -> str:
    """
    Ask the fine-tuned medical chatbot a question.

    Note: prompt format must exactly match training format — same system prompt,
    same [INST]...[/INST] template, NO MCQ options in the question.
    """
    prompt = (
        f'<s>[INST] {SYSTEM_PROMPT}\n\n'
        f'Question: {question} [/INST] '
    )

    inputs = tokenizer(
        prompt,
        return_tensors = 'pt',
        truncation     = True,
        max_length     = MAX_SEQ_LEN
    ).to(model.device)

    with torch.no_grad():
        outputs = model.generate(
            **inputs,
            max_new_tokens     = max_new_tokens,
            temperature        = temperature,
            do_sample          = temperature > 0,
            top_p              = 0.9,
            repetition_penalty = 1.15,
            eos_token_id       = tokenizer.eos_token_id,
            pad_token_id       = tokenizer.pad_token_id,
            use_cache          = True,
        )

    full_output = tokenizer.decode(outputs[0], skip_special_tokens=True)
    answer = full_output.split('[/INST]')[-1].strip()
    return answer


