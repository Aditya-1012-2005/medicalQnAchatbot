"""---
## 🧹 Section 4 — Data Cleaning & EDA

### Critical Fix — MedAlpaca MCQ Format

MedAlpaca stores USMLE-style questions in `input` like:
```
A 45-year-old male presents with chest pain...
A) Aspirin
B) Metoprolol   ← these options appear in training but NOT at inference
C) Nitroglycerin
D) Heparin
```
And `output` like: `"The answer is (C). Nitroglycerin works by..."`

**Problem:** At inference you pass a clean question without options → train/inference distribution mismatch → garbage output.  
**Fix:** Strip MCQ options from questions, strip answer-letter prefix from answers.
"""

def clean_text(text):
    """Universal text cleaner."""
    if not isinstance(text, str):
        return None
    text = text.strip()
    text = re.sub(r'\s+', ' ', text)
    text = re.sub(r'\\n|\\t', ' ', text)
    text = re.sub(r'<.*?>', '', text)
    text = re.sub(r'http\S+', '', text)
    text = re.sub(r'[^\x00-\x7F]+', ' ', text)
    text = re.sub(r'\s+', ' ', text).strip()
    return text if len(text) > 10 else None


def strip_mcq_options(text):
    """
    Remove MCQ option lines like:
      A) option text
      (B) option text
      A. option text
    from the question body so inference distribution matches training.
    """
    if not isinstance(text, str):
        return text
    # Remove lines that start with option labels
    text = re.sub(r'\b[A-E][\)\.]\s+[^\n]+', '', text)
    text = re.sub(r'\([A-E]\)\s+[^\n]+', '', text)
    return re.sub(r'\s+', ' ', text).strip()


def strip_answer_prefix(text):
    """
    Remove USMLE-style answer letter prefix:
      'The answer is (C). ...' → '...'
      'C) ...'                 → '...'
    """
    if not isinstance(text, str):
        return text
    text = re.sub(r'^(The correct answer is|The answer is|Answer:)\s*\(?[A-E]\)?[\s\.\-:]*', '', text, flags=re.IGNORECASE)
    text = re.sub(r'^[A-E][\)\.]\s*', '', text)
    return text.strip()


def clean_medalpaca(df):
    df = df.copy()

    # 'input' = question body (may include MCQ options), 'output' = answer
    if 'input' not in df.columns or 'output' not in df.columns:
        raise ValueError("Expected 'input' and 'output' columns")

    df = df.rename(columns={'input': 'question', 'output': 'answer'})[['question', 'answer']]

    df['question'] = df['question'].apply(clean_text).apply(strip_mcq_options)
    df['answer']   = df['answer'].apply(clean_text).apply(strip_answer_prefix)

    df = df.dropna(subset=['question', 'answer'])
    df['question'] = df['question'].astype(str)
    df['answer']   = df['answer'].astype(str)

    df = df[df['question'].str.len() >= 30]  # must be a real question
    df = df[df['answer'].str.len()   >= 40]  # must have real explanation
    df = df[df['answer'].str.len()   <= 4000]
    df = df.drop_duplicates(subset=['question'])

    df['source'] = 'medalpaca_medqa'
    return df.reset_index(drop=True)


